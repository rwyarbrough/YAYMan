    private void lstNmtValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstNmtValueChanged
        if (lstNmt.getSelectedIndex() == -1) {
            lblNmtName.setText("[Name]");
            lblNmtAddress.setText("[Address]");
            currentNmt = null;
            popAPI = null;
            return;
        }

        currentNmt = (NetworkedMediaTank)lstNmt.getSelectedValue();
        lblNmtName.setText(currentNmt.getName());
        lblNmtAddress.setText(currentNmt.getAddress());
        popAPI = new PopAPI(currentNmt.getAddress());
        updateShareList();
    }

