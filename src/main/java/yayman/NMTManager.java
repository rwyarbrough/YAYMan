    public NMTManager(String nmtName) {
        this();

        for(int i = 0; i < nmtModel.getSize(); i++) {
            NetworkedMediaTank nmt = (NetworkedMediaTank)nmtModel.get(i);
            if (nmt.getName().equals(nmtName)) {
                lstNmt.setSelectedIndex(i);
            }
        }

        if (lstNmt.getSelectedIndex() == -1 && nmtModel.getSize() > 0) lstNmt.setSelectedIndex(0);
    }

